    def _generate_json(self,mag:MutationArtifactGraph,stem:str,stage2_report:Optional[Any]=None)->str:
        """
        Generate a SIEM-ready JSON report with the full MAG serialisation
        plus a high-level executive summary section.
        """
        passed=mag.high_confidence_forecasts(CONFIDENCE_GATE_THRESHOLD)
        full_mag=mag.to_dict()
        research_summary=full_mag.get("research_readiness")or{}
        stage2_summary=full_mag.get("stage2_intelligence")or self._stage2_to_dict(stage2_report)
        report={
            "oracle_tmf_version":"1.0.0",
            "report_generated_utc":datetime.now(timezone.utc).isoformat(),
            "research_readiness":research_summary,
            "stage2_summary":stage2_summary,
            "executive_summary":{
                "apk_sha256":mag.apk_metadata.sha256,
                "package_name":mag.apk_metadata.package_name,
                "malware_family":mag.malware_family,
                "family_version":mag.family_version,
                "total_artifacts":mag.total_artifact_count(),
                "artifact_classes":mag.artifact_class_counts(),
                "forecasts_generated":len(mag.forecasts),
                "forecasts_passed_gate":len(passed),
                "highest_confidence":max((f.confidence_score for f in mag.forecasts),default=0.0),
                "predicted_techniques":[f.predicted_technique for f in passed],
            },
            "full_mag":full_mag,
        }
        path=os.path.join(REPORT_OUTPUT_DIR,f"{stem}_report.json")
        with open(path,"w",encoding="utf-8")as fh:
            json.dump(report,fh,indent=2,default=str)
        logger.debug("[Stage L] JSON report written: %s",path)
        return path
    def _generate_yara(self,mag:MutationArtifactGraph,stem:str)->str:
        """
        Generate proactive YARA detection rules for each high-confidence forecast.
        Rule philosophy:
          ÃƒÆ’Ã‚Â¢ÃƒÂ¢Ã¢â‚¬Å¡Ã‚Â¬Ãƒâ€šÃ‚Â¢ Strings section uses artifact evidence extracted from the current APK
          ÃƒÆ’Ã‚Â¢ÃƒÂ¢Ã¢â‚¬Å¡Ã‚Â¬Ãƒâ€šÃ‚Â¢ These strings are the SCAFFOLDING that will evolve into the next
            active payload ÃƒÆ’Ã‚Â¢ÃƒÂ¢Ã¢â‚¬Å¡Ã‚Â¬ÃƒÂ¢Ã¢â€šÂ¬Ã‚Â detecting them now provides lead-time before the
            threat emerges
          ÃƒÆ’Ã‚Â¢ÃƒÂ¢Ã¢â‚¬Å¡Ã‚Â¬Ãƒâ€šÃ‚Â¢ Condition requires ZIP magic (APK = ZIP) + multiple artifact signals
        """
        rules_text=self._yara_file_header(mag)
        passed=mag.high_confidence_forecasts(CONFIDENCE_GATE_THRESHOLD)
        if not passed:
            rules_text+=self._yara_fingerprint_rule(mag)
        else:
            for i,forecast in enumerate(passed,1):
                rules_text+=self._yara_forecast_rule(
                    mag,forecast,rule_index=i)
        path=os.path.join(REPORT_OUTPUT_DIR,f"{stem}_forecast.yar")
        with open(path,"w",encoding="utf-8")as fh:
            fh.write(rules_text)
        logger.debug("[Stage L] YARA rules written: %s (%d rule(s))",
                     path,max(1,len(passed)))
        return path
    def _yara_file_header(self,mag:MutationArtifactGraph)->str:
        """YARA file comment block header."""
        return(
            f"//ORACLE-TMF Proactive YARA Ruleset\n"
            f"//Generated:{datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S UTC')}\n"
            f"//APK SHA256:{clean_text(mag.apk_metadata.sha256,80)}\n"
            f"//Package:{clean_text(mag.apk_metadata.package_name,200)}\n"
            f"//Family:{clean_text(mag.malware_family or 'UNKNOWN',120)}\n"
            f"//WARNING:These rules are PROACTIVE forecasts for v_{{n+1}}.\n"
            f"//Deployment before v_{{n+1}}discovery provides temporal advantage.\n"
            f"//Gate threshold applied:C>{CONFIDENCE_GATE_THRESHOLD}\n\n"
        )
    def _yara_forecast_rule(
        self,mag:MutationArtifactGraph,forecast:MutationForecast,rule_index:int
    )->str:
        """Generate one safely escaped YARA rule for a single forecast."""
        family_clean=_yara_identifier(mag.malware_family or "UNKNOWN")
        technique_clean=_yara_identifier(forecast.predicted_technique or "UNKNOWN")
        confidence_pct=int(forecast.confidence_score*100)
        rule_name=f"ORACLE_TMF_{family_clean}_{technique_clean}_Forecast_{confidence_pct:02d}_{rule_index}"
        meta_block=(
            f'    meta:\n'
            f'        oracle_tmf_version="1.0.0"\n'
            f'        description="ORACLE-TMF proactive forecast:{_yara_string(mag.malware_family)}->{_yara_string(forecast.predicted_technique)}({_yara_string(forecast.technique_name)})"\n'
            f'        author="{_yara_string(STIX_IDENTITY_NAME)}"\n'
            f'        date="{datetime.now(timezone.utc).strftime("%Y-%m-%d")}"\n'
            f'        confidence="{forecast.confidence_score:.3f}"\n'
            f'        mitre_tactic="{_yara_string(forecast.predicted_tactic)}"\n'
            f'        mitre_technique="{_yara_string(forecast.predicted_technique)}-{_yara_string(forecast.technique_name)}"\n'
            f'        malware_family="{_yara_string(mag.malware_family)}"\n'
            f'        apk_sha256_analyzed="{_yara_string(mag.apk_metadata.sha256,80)}"\n'
            f'        forecast_status="PROACTIVE_v_n_plus_1"\n'
        )
        if forecast.predicted_target_institutions:
            targets=_yara_string(", ".join(forecast.predicted_target_institutions[:3]))
            meta_block+=f'        predicted_targets="{targets}"\n'
        strings_lines:list[str]=[]
        for i,artifact in enumerate(mag.dead_code[:3],1):
            token=_yara_string(artifact.method_name.split("(")[0],40).strip()
            if token and len(token)>4:
                strings_lines.append(f'        $dead_code_{i}="{token}" ascii nocase')
        for i,stub in enumerate(mag.c2_stubs[:2],1):
            if stub.extracted_url:
                strings_lines.append(f'        $c2_stub_{i}="{_yara_string(stub.extracted_url,60)}" ascii')
            if stub.framework:
                strings_lines.append(f'        $c2_framework_{i}="{_yara_string(stub.framework,80)}" ascii')
        for i,perm in enumerate(mag.unused_permissions[:2],1):
            strings_lines.append(f'        $unused_perm_{i}="{_yara_string(perm.permission_name,120)}" ascii')
        for i,ps in enumerate(sorted(mag.placeholder_strings,key=lambda x:x.entropy,reverse=True)[:2],1):
            if ps.entropy>4.5 and len(ps.value)<80:
                strings_lines.append(f'        $placeholder_{i}="{_yara_string(ps.value,60)}" ascii')
        if not strings_lines:
            family_marker=_yara_string(mag.malware_family or "UNKNOWN",80)
            strings_lines.append(f'        $family_marker="{family_marker}" ascii nocase')
        strings_block="    strings:\n"+"\n".join(strings_lines)+"\n"
        condition_block=(
            f"    condition:\n"
            f"        uint16(0)==0x6B50 and//APK/ZIP magic bytes\n"
            f"        filesize<100MB and\n"
            f"(\n"
            f"            2 of($dead_code_*)or\n"
            f"(1 of($c2_stub_*)and 1 of($unused_perm_*))\n"
            f")\n"
        )
        return f"rule{rule_name}\n{{\n{meta_block}{strings_block}{condition_block}}}\n\n"
    def _yara_fingerprint_rule(self,mag:MutationArtifactGraph)->str:
        """Generic APK fingerprint rule when no forecast passes the gate."""
        family_clean=_yara_identifier(mag.malware_family or "UNKNOWN")
        rule_name=f"ORACLE_TMF_{family_clean}_APK_Fingerprint"
        return(
            f"rule{rule_name}\n"
            f"{{\n"
            f"    meta:\n"
            f'        description="ORACLE-TMF APK fingerprint-no high-confidence forecast"\n'
            f'        apk_sha256="{_yara_string(mag.apk_metadata.sha256,80)}"\n'
            f'        package="{_yara_string(mag.apk_metadata.package_name)}"\n'
            f"    condition:\n"
            f'        uint16(0)==0x6B50 and\n'
            f'        hash.sha256(0,filesize)=="{_yara_string(mag.apk_metadata.sha256,80)}"\n'
            f"}}\n\n"
        )
