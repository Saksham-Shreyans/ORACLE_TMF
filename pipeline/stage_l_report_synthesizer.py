    STAGE_NAME="STAGE_L"
    _REPORT_TTL_SECONDS:int=int(os.getenv("ORACLE_TMF_REPORT_TTL_SECONDS",str(24*3600)))
    def __init__(self)->None:
        Path(REPORT_OUTPUT_DIR).mkdir(parents=True,exist_ok=True)
        self._purge_stale_reports()
    def _purge_stale_reports(self)->None:
        """V-013: Delete report files older than _REPORT_TTL_SECONDS."""
        import time as _time
        cutoff=_time.time()-self._REPORT_TTL_SECONDS
        report_dir=Path(REPORT_OUTPUT_DIR)
        for f in report_dir.iterdir():
            if f.is_file():
                try:
                    if f.stat().st_mtime<cutoff:
                        f.unlink(missing_ok=True)
                        logger.debug("[Stage L] Purged stale report: %s",f.name)
                except OSError:
                    pass
